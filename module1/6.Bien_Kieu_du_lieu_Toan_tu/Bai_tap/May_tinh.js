


function Addition(){
    let inputA = parseInt(document.getElementById("inputA").value);
    let inputB = parseInt(document.getElementById("inputB").value);
    let addition;
    addition = inputA + inputB;
    document.getElementById("result").innerText= "Result Division: " + addition;


}

